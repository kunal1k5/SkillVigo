function buildDate(daysFromNow, hours, minutes = 0) {
  const date = new Date();
  date.setDate(date.getDate() + daysFromNow);
  date.setHours(hours, minutes, 0, 0);
  return date.toISOString();
}

function buildTimestamp(offsetMinutes) {
  return new Date(Date.now() + offsetMinutes * 60 * 1000).toISOString();
}

function buildMessage(id, conversationId, senderRole, senderName, content, offsetMinutes, deliveryStatus = 'Seen') {
  return {
    id,
    conversationId,
    senderRole,
    senderName,
    content,
    createdAt: buildTimestamp(offsetMinutes),
    isRead: senderRole === 'me',
    deliveryStatus,
  };
}

